let texto = 'Texto para conversão';

// Convertendo para maiúsculas
let maiusculas = texto.toLocaleUpperCase();

console.log(maiusculas); // Retorna 'TEXTO PARA CONVERSÃP'

// Convertendo para minúsculas
let minusculas = texto.toLowerCase();
console.log(minusculas); // Retorna 'texto para conversão'